CREATE TABLE auth_totp_credential (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id BIGINT NOT NULL UNIQUE REFERENCES auth_user (id) ON DELETE CASCADE,
    secret_ciphertext TEXT NOT NULL,
    enabled_at TIMESTAMP WITH TIME ZONE NULL,
    created_at TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE auth_totp_backup_code (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    totp_credential_id BIGINT NOT NULL REFERENCES auth_totp_credential (id) ON DELETE CASCADE,
    code_hash VARCHAR(100) NOT NULL,
    used_at TIMESTAMP WITH TIME ZONE NULL,
    created_at TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE INDEX idx_auth_totp_backup_code_unused
    ON auth_totp_backup_code (totp_credential_id)
    WHERE used_at IS NULL;
